def load_real_kopis_data() -> pd.DataFrame:
    """전국 지역별 연간(2021~2025) 실제 KOPIS 데이터를 DataFrame으로 반환."""
    rows = []
    for region, years in _RAW.items():
        for year, (cnt, sessions, tickets, sales_thousand_krw) in years.items():
            rows.append({
                "연도": year,
                "지역": region,
                "공연건수": cnt,
                "공연회차": sessions,
                "티켓예매수": tickets,
                "티켓판매액(천원)": sales_thousand_krw,
            })
    df = pd.DataFrame(rows).sort_values(["연도", "지역"]).reset_index(drop=True)
    return df


if __name__ == "__main__":
    df = load_real_kopis_data()
    df.to_csv("kopis_region_yearly_real.csv", index=False, encoding="utf-8-sig")
    print(f"저장 완료: kopis_region_yearly_real.csv ({len(df)}행)")
